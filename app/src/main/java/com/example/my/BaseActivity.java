    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        getMenuInflater().inflate(R.menu.main_menu, menu);

        // Check if the current activity is EtypListActivity
        if (!(this instanceof EtypListActivity)) {
            // Remove the transaction history item if not in EtypListActivity
            menu.findItem(R.id.action_transaction_history).setVisible(false);
        }
        return true;
    }


    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        int id = item.getItemId();

        // Navigate to SettingsActivity when the "Settings" option is selected
        if (id == R.id.action_settings) {
            Intent intent = new Intent(this, SettingsActivity.class);
            startActivity(intent);
            return true;
        } else if (id == R.id.action_transaction_history) {
            // Launch the TransactionHistoryActivity to view transaction history
            Intent intent = new Intent(this, TransactionHistoryActivity.class);
            startActivity(intent);
            return true;
        }

        return super.onOptionsItemSelected(item);
    }


